/* Portable host fallback (non-MIPS builds): the observable behaviour is an n-byte memmove
   with the (src, dst, n) argument order. */
extern void blockmove(void *src, void *dst, int n)
{
    char *s = (char *)src;
    char *d = (char *)dst;
    if (s < d) { s += n; d += n; while (n-- > 0) *--d = *--s; }
    else       { while (n-- > 0) *d++ = *s++; }
}
