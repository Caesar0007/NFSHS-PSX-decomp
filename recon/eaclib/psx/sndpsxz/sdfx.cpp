/* iSNDpsxfxinit @0x8010020C : load the reverb preset `mode` into the SPU -- program the 0x20 reverb
 *   coefficient registers, set the reverb work-area base, DMA-clear the reverb RAM, then enable reverb.
 *   Modes 5/10/20/30/40/50/100/110/120 map to preset indices; modes 7/8 scale a few taps by sndgs depth. */
extern "C" unsigned int iSNDpsxfxinit(int mode)
{
    unsigned char scratch[256];
    short         rv[0x21];
    unsigned char *src;
    int           i, work;
    unsigned int  ctl;
    /* MATCH: oracle materializes `s2 = &sndgs[0x27]` (D_801478FC == sndgs+0x9C) unconditionally in the
     * PROLOGUE, before even the mode==0 check -- a callee-saved value the scheduler slots in early
     * alongside the other prologue register spills, reused later (via +8/+0xC) for all 7 reverb-depth
     * taps. Declaring it here (matching that early C-source position) instead of inside the `if` block
     * it's actually used in reproduces that hoist. */
    int *sg = &sndgs[0x27];

    if (mode != 0) {
        if      (mode == 5)    mode = 1;
        else if (mode == 10)   mode = 2;
        else if (mode == 0x14) mode = 3;
        else if (mode == 0x1e) mode = 4;
        else if (mode == 0x28) mode = 5;
        else if (mode == 0x32) mode = 6;
        else if (mode == 100)  mode = 9;
        else if (mode == 0x6e) mode = 8;
        else if (mode == 0x78) mode = 7;
    }
    /* MATCH: oracle materializes `s3 = &sndpd` right here (after the mode-switch resolves, before the
     * preset-table src pointer), reused later for the reverb-depth write below AND the DMA-clear loop
     * further down -- same early-hoist lever as `sg` above. */
    unsigned char *pd = sndpd;
    src = &snd_reverb_table[mode * 0x42];
    *(unsigned short *)(pd + 0x51E) = (short)((int)(0x10000 - (unsigned int)*(unsigned short *)src) >> 3);
    iSNDpsxeffectoff(0xffffff);
    iSNDpsxeffectvol(0, 0);
    blockmove((int *)src, (int *)rv, 0x42);

    if ((unsigned int)(mode - 7U) < 2) {                  /* presets 7/8: scale taps by reverb depth */
        rv[0xb] = (short)((sg[2] * 0x2000) / 0x7f) - rv[1];
        rv[0xc] = (short)((sg[2] * 0x1000) / 0x7f) - rv[2];
        rv[0xd] = rv[0xe] + (short)((sg[2] * 0x1000) / 0x7f);
        rv[0x11] = rv[0x12] + (short)((sg[2] * 0x1000) / 0x7f);
        rv[0x1b] = rv[0x1d] + (short)((sg[2] * 0x1000) / 0x7f);
        rv[0x1c] = rv[0x1e] + (short)((sg[2] * 0x1000) / 0x7f);
        rv[8] = (short)((sg[3] * 0x8100) / 0x7f);
    }

    *(unsigned short *)(DAT_80147e2c + 0x1aa) = *(unsigned short *)(DAT_80147e2c + 0x1aa) & 0xff7f;  /* reverb off while loading */
    for (i = 0; i < 0x20; i++)                            /* 0x20 reverb coefficient regs */
        *(unsigned short *)(i * 2 + DAT_80147e2c + 0x1c0) = (unsigned short)rv[1 + i];
    work = 0x10000 - (unsigned short)rv[0];
    *(short *)(DAT_80147e2c + 0x1a2) = (short)work;        /* reverb work-area start address */

    blockclear((int)scratch, 0x100);                      /* zero the 256-byte DMA scratch */
    i = 0;                                                /* DMA-clear SPU reverb RAM in 0x20-word runs */
    while (i < 0x10000) {
        int chunk = 0x10000 - i;
        if (0x20 < chunk) chunk = 0x20;
        int h = iSNDdmqueue((int)scratch, (unsigned int)(i << 3), chunk << 3, 1, 0);
        i += 0x20;
        do { } while (iSNDdmcomplete(h) == 0);
    }

    ctl = *(unsigned short *)(DAT_80147e2c + 0x1aa) | 0x80;   /* reverb enable */
    *(short *)(DAT_80147e2c + 0x1aa) = (short)ctl;
    return ctl;
}

/* iSNDplatformfxinit @0x80100584 : record the requested fx `mode` and, if audio is up, apply it now. */
/* near-miss floor (11 diffs): oracle's `lui v0,%hi(D_8014791c)` (computed early, before the
 * `sndgs[0xf]` branch) is REUSED as the store's base in the branch's delay slot (`sw a0,%lo(...)(v0)`);
 * our gcc materializes a FRESH `$at` scratch for the store instead of reusing an already-live v0, and
 * reads the guard byte into v0 (ours) vs v1 (oracle). Tried: store-in-branch+early-return (regressed,
 * duplicated the store, 17 diffs), an explicit `int *slot=&DAT_...` local (no change), pre-reading the
 * guard into a named `signed char` (regressed, +1 insn re-extension), reordering the `(void)reserved;`
 * no-op (no change, as expected). Pure gcc-2.8.0 scratch-register/scheduling tie-break across the
 * branch; not source-reachable. Permuter multi-basin candidate. */
extern "C" int iSNDplatformfxinit(int reserved, int mode)
{
    (void)reserved;
    DAT_8014791c = mode;
    if ((signed char)sndgs[0xf] != 0)
        iSNDpsxfxinit(mode);
    return 0;
}
